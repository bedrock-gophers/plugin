package main

import (
	"bufio"
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strconv"
	"strings"
)

type entry struct {
	Const string
	Name  string
}

func main() {
	entries, err := readEntries("keys.txt")
	if err != nil {
		fail("read keys", err)
	}
	if len(entries) == 0 {
		fail("read keys", fmt.Errorf("no key entries found"))
	}

	var b bytes.Buffer
	b.WriteString("// Code generated by go generate; DO NOT EDIT.\n")
	b.WriteString("package ctxkey\n\n")
	b.WriteString("const (\n")
	for i, e := range entries {
		id := strconv.Itoa(i + 1)
		b.WriteString("\t" + e.Const + " uint32 = " + id + "\n")
	}
	b.WriteString(")\n\n")
	b.WriteString("var byName = map[string]uint32{\n")
	for _, e := range entries {
		b.WriteString("\t\"" + e.Name + "\": " + e.Const + ",\n")
	}
	b.WriteString("}\n\n")
	b.WriteString("func ID(name string) (uint32, bool) {\n")
	b.WriteString("\tid, ok := byName[name]\n")
	b.WriteString("\treturn id, ok\n")
	b.WriteString("}\n")

	outPath := filepath.Join("generated.go")
	if err := os.WriteFile(outPath, b.Bytes(), 0644); err != nil {
		fail("write generated file", err)
	}
}

func readEntries(path string) ([]entry, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()

	s := bufio.NewScanner(f)
	var out []entry
	lineNo := 0
	seenConst := map[string]struct{}{}
	seenName := map[string]struct{}{}
	for s.Scan() {
		lineNo++
		line := strings.TrimSpace(s.Text())
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		parts := strings.Fields(line)
		if len(parts) != 2 {
			return nil, fmt.Errorf("line %d: expected '<ConstName> <key>'", lineNo)
		}
		e := entry{Const: parts[0], Name: parts[1]}
		if _, ok := seenConst[e.Const]; ok {
			return nil, fmt.Errorf("line %d: duplicate const %q", lineNo, e.Const)
		}
		if _, ok := seenName[e.Name]; ok {
			return nil, fmt.Errorf("line %d: duplicate key %q", lineNo, e.Name)
		}
		seenConst[e.Const] = struct{}{}
		seenName[e.Name] = struct{}{}
		out = append(out, e)
	}
	if err := s.Err(); err != nil {
		return nil, err
	}
	return out, nil
}

func fail(step string, err error) {
	fmt.Fprintf(os.Stderr, "%s: %v\n", step, err)
	os.Exit(1)
}
