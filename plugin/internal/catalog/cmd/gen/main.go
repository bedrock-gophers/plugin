package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"unicode"

	_ "github.com/df-mc/dragonfly/server/block"
	_ "github.com/df-mc/dragonfly/server/item"
	"github.com/df-mc/dragonfly/server/world"
)

type entry struct {
	Name string
	ID   string
}

func main() {
	items := collectItemIDs()
	blocks := collectBlockIDs()
	worlds := collectWorldIDs()

	goPath := filepath.Join("..", "..", "sdk", "go", "ids_generated.go")
	csPath := filepath.Join("..", "..", "sdk", "csharp", "src", "Abi", "Ids.g.cs")

	if err := writeFormattedGo(goPath, renderGo(items, blocks, worlds)); err != nil {
		fail("generate go ids", err)
	}
	if err := os.WriteFile(csPath, renderCSharp(items, blocks, worlds), 0644); err != nil {
		fail("generate csharp ids", err)
	}
}

func collectItemIDs() []entry {
	seen := map[string]struct{}{}
	values := make([]string, 0, 1024)
	for _, it := range world.Items() {
		if it == nil {
			continue
		}
		name, _ := it.EncodeItem()
		name = strings.TrimSpace(name)
		if name == "" {
			continue
		}
		key := strings.ToLower(name)
		if _, ok := seen[key]; ok {
			continue
		}
		seen[key] = struct{}{}
		values = append(values, name)
	}
	sort.Strings(values)
	return buildEntries(values)
}

func collectBlockIDs() []entry {
	seen := map[string]struct{}{}
	values := make([]string, 0, 1024)
	for _, block := range world.Blocks() {
		if block == nil {
			continue
		}
		name, _ := block.EncodeBlock()
		name = strings.TrimSpace(name)
		if name == "" {
			continue
		}
		key := strings.ToLower(name)
		if _, ok := seen[key]; ok {
			continue
		}
		seen[key] = struct{}{}
		values = append(values, name)
	}
	sort.Strings(values)
	return buildEntries(values)
}

func collectWorldIDs() []entry {
	return buildEntries([]string{"overworld", "nether", "end"})
}

func buildEntries(ids []string) []entry {
	out := make([]entry, 0, len(ids))
	used := map[string]int{}
	for _, id := range ids {
		name := symbolForID(id)
		count := used[name]
		used[name] = count + 1
		if count > 0 {
			name = fmt.Sprintf("%s_%d", name, count+1)
		}
		out = append(out, entry{Name: name, ID: id})
	}
	return out
}

func symbolForID(id string) string {
	id = strings.TrimSpace(strings.ToLower(id))
	id = strings.TrimPrefix(id, "minecraft:")

	var parts []string
	var cur strings.Builder
	flush := func() {
		if cur.Len() == 0 {
			return
		}
		parts = append(parts, cur.String())
		cur.Reset()
	}

	for _, r := range id {
		if (r >= 'a' && r <= 'z') || (r >= '0' && r <= '9') {
			cur.WriteRune(r)
			continue
		}
		flush()
	}
	flush()

	if len(parts) == 0 {
		return "Unknown"
	}

	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		runes := []rune(p)
		runes[0] = unicode.ToUpper(runes[0])
		b.WriteString(string(runes))
	}
	name := b.String()
	if name == "" {
		name = "Unknown"
	}
	if name[0] >= '0' && name[0] <= '9' {
		name = "N" + name
	}
	return name
}

func renderGo(items, blocks, worlds []entry) []byte {
	var b bytes.Buffer
	b.WriteString("// Code generated by go generate; DO NOT EDIT.\n")
	b.WriteString("package guest\n\n")
	b.WriteString("type ItemID string\n")
	b.WriteString("type BlockID string\n\n")
	b.WriteString("type WorldID string\n\n")

	b.WriteString("const (\n")
	for _, e := range items {
		fmt.Fprintf(&b, "\tItem%s ItemID = %q\n", e.Name, e.ID)
	}
	b.WriteString(")\n\n")

	b.WriteString("const (\n")
	for _, e := range blocks {
		fmt.Fprintf(&b, "\tBlock%s BlockID = %q\n", e.Name, e.ID)
	}
	b.WriteString(")\n\n")

	b.WriteString("const (\n")
	for _, e := range worlds {
		fmt.Fprintf(&b, "\tWorld%s WorldID = %q\n", e.Name, e.ID)
	}
	b.WriteString(")\n\n")

	b.WriteString("var AllItemIDs = []ItemID{\n")
	for _, e := range items {
		fmt.Fprintf(&b, "\tItem%s,\n", e.Name)
	}
	b.WriteString("}\n\n")

	b.WriteString("var AllBlockIDs = []BlockID{\n")
	for _, e := range blocks {
		fmt.Fprintf(&b, "\tBlock%s,\n", e.Name)
	}
	b.WriteString("}\n")

	b.WriteString("\n")
	b.WriteString("var AllWorldIDs = []WorldID{\n")
	for _, e := range worlds {
		fmt.Fprintf(&b, "\tWorld%s,\n", e.Name)
	}
	b.WriteString("}\n")
	return b.Bytes()
}

func renderCSharp(items, blocks, worlds []entry) []byte {
	var b bytes.Buffer
	b.WriteString("// Code generated by go generate; DO NOT EDIT.\n")
	b.WriteString("namespace BedrockPlugin.Sdk.Guest\n{\n")
	b.WriteString("public static partial class Item\n{\n")
	for _, e := range items {
		fmt.Fprintf(&b, "    public const string %s = %q;\n", e.Name, e.ID)
	}
	b.WriteString("}\n")
	b.WriteString("}\n\n")

	b.WriteString("namespace BedrockPlugin.Sdk.Abi\n{\n")

	b.WriteString("public static class BlockIds\n{\n")
	for _, e := range blocks {
		fmt.Fprintf(&b, "    public const string %s = %q;\n", e.Name, e.ID)
	}
	b.WriteString("}\n")

	b.WriteString("\n")
	b.WriteString("public static class WorldIds\n{\n")
	for _, e := range worlds {
		fmt.Fprintf(&b, "    public const string %s = %q;\n", e.Name, e.ID)
	}
	b.WriteString("}\n")
	b.WriteString("}\n")
	return b.Bytes()
}

func writeFormattedGo(path string, src []byte) error {
	formatted, err := format.Source(src)
	if err != nil {
		return fmt.Errorf("format source for %s: %w", path, err)
	}
	if err := os.WriteFile(path, formatted, 0644); err != nil {
		return fmt.Errorf("write %s: %w", path, err)
	}
	return nil
}

func fail(step string, err error) {
	fmt.Fprintf(os.Stderr, "%s: %v\n", step, err)
	os.Exit(1)
}
